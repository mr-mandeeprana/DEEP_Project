-- Fix security warnings by adding search_path to functions
CREATE OR REPLACE FUNCTION public.update_post_drafts_updated_at()
RETURNS TRIGGER 
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

